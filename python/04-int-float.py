# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# Positivo ou Negativo. int sem sinal é considerado positivo.
print(11) # int
print(-11) # int
print(0) # int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número.
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11) # float
print(0.0, -1.5) # float

# A função type mostra o tippo que o Python inferiu ao valor.

print(type('Leticia')) # type string -> str 
print(type(10)) # type int
print(type(5.5)) # float