"""
    Python = Linguagem de programação
    Tipo de tipagem = Dinâmica / Forte
    str --> string --> texto
    Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Leticia Cibele')
print('Leticia "Cibele"') # exibindo aspas duplas 

# Aspas duplas
print("Leticia Cibele")
print("Leticia 'Cibele'") # exibindo aspas simples

# Escape
print("Leticia \"Cibele\"") # \ escape, pula a aspa depois da \

# r
print(r"Leticia \"Cibele\"") # Usado em expressões regulares