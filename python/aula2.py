# função para exibir coisas na tela, a função recebe argumentos.
print(12, 34) # os argumentos são separados por virgula (argumentos não nomeados são separados por virgula).

print(56, 78, sep='-') # Utilizando outro separador com sep (pode ser qualquer coisa).

print(9, 10, sep='-', end='\n##') # final do print, depois do ultimo argumento.

# Quebra de linha \n
print(1,'\n',2)